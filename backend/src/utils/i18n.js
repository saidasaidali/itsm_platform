export const getLang = (req) => {
  const raw = req?.lang || req?.headers?.['accept-language'] || 'fr';
  const lang = String(raw).split(',')[0].split('-')[0].trim().toLowerCase();
  return ['fr', 'ar', 'en'].includes(lang) ? lang : 'fr';
};

export const t = (req, key, params = {}) => {
  const lang = getLang(req);
  let text = MESSAGES[lang]?.[key] ?? MESSAGES.fr[key] ?? key;
  Object.entries(params).forEach(([k, v]) => {
    text = text.replace(new RegExp(`\\{\\{${k}\\}\\}`, 'g'), String(v));
  });
  return text;
};

export default { t, getLang };
